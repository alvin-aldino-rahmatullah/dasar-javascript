// arithmetic expression adalah ekspresi yang melibatkan operasi matematika
// hasil dari arithmetic expression adalah sebuah nilai numerik (number)

const x = 20;
const y = 4;

const hasilTambah = x + y;     
const hasilKurang = x - y;
const hasilKali = x * y;
const hasilBagi = x / y;
const hasilModulus = x % y;

console.log("hasil x + y =", hasilTambah);
console.log("hasil x - y =", hasilKurang);
console.log("hasil x * y =", hasilKali);
console.log("hasil x / y =", hasilBagi);
console.log("hasil x % y =", hasilModulus);

// 1. JavaScript membaca variabel x dan y
// 2. JavaScript membaca operator aritmatika
// 3. JavaScript melakukan perhitungan
// 4. JavaScript menghasilkan NILAI NUMBER
