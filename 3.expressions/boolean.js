// boolean expression adalah ekspresi yang menghasilkan nilai true atau false
// biasanya menggunakan operator perbandingan seperti: > < >= <= === !==

const umur = 20;

// apakah nilai umur (20) lebih besar atau sama dengan 18?
// jika iya maka true
// jika tidak maka false
const isDewasa = umur >= 18;   
// apakah nilai umur (20) lebih kecil dari 18?
const isAnak = umur < 18;

console.log("apakah umur >= 18?", isDewasa);
console.log("apakah umur < 18?", isAnak);